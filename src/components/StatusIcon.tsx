export function ChevronIcon({ direction = 'right', size = 16, className = '' }: { direction?: 'up' | 'down' | 'left' | 'right'; size?: number; className?: string }) {
    const rotations = {
        up: 'rotate(-90deg)',
        down: 'rotate(90deg)',
        left: 'rotate(180deg)',
        right: 'rotate(0deg)',
    };

    return (
        <svg
            xmlns="http://www.w3.org/2000/svg"
            width={size}
            height={size}
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
            className={className}
            style={{ transform: rotations[direction], transition: 'transform 0.2s ease' }}
        >
            <polyline points="9 18 15 12 9 6" />
        </svg>
    );
}


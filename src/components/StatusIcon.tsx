import React from 'react';

type StatusType = 'nao_sei' | 'aprendendo' | 'dominada';

interface StatusIconProps {
    status: StatusType;
    variant?: 'icon' | 'dot';
    className?: string;
    size?: number;
}

const statusColors = {
    nao_sei: '#f75a68',
    aprendendo: '#fba94c',
    dominada: '#04d361',
};

export function StatusIcon({ status, variant = 'icon', className = '', size = 20 }: StatusIconProps) {
    const color = statusColors[status];

    // Variante dot - c√≠rculo colorido simples
    if (variant === 'dot') {
        return (
            <svg
                xmlns="http://www.w3.org/2000/svg"
                width={size}
                height={size}
                viewBox="0 0 24 24"
                fill={color}
                className={className}
            >
                <circle cx="12" cy="12" r="6" />
            </svg>
        );
    }

    // Variante icon - √≠cones detalhados
    if (status === 'dominada') {
        // CheckCircle
        return (
            <svg
                xmlns="http://www.w3.org/2000/svg"
                width={size}
                height={size}
                viewBox="0 0 24 24"
                fill="none"
                stroke={color}
                strokeWidth="2"
                strokeLinecap="round"
                strokeLinejoin="round"
                className={className}
            >
                <path d="M22 11.08V12a10 10 0 1 1-5.93-9.14" />
                <polyline points="22 4 12 14.01 9 11.01" />
            </svg>
        );
    }

    if (status === 'aprendendo') {
        // Clock / Em progresso
        return (
            <svg
                xmlns="http://www.w3.org/2000/svg"
                width={size}
                height={size}
                viewBox="0 0 24 24"
                fill="none"
                stroke={color}
                strokeWidth="2"
                strokeLinecap="round"
                strokeLinejoin="round"
                className={className}
            >
                <circle cx="12" cy="12" r="10" />
                <polyline points="12 6 12 12 16 14" />
            </svg>
        );
    }

    // nao_sei - Circle vazio
    return (
        <svg
            xmlns="http://www.w3.org/2000/svg"
            width={size}
            height={size}
            viewBox="0 0 24 24"
            fill="none"
            stroke={color}
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
            className={className}
        >
            <circle cx="12" cy="12" r="10" />
        </svg>
    );
}

